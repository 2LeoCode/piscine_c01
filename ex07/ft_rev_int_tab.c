#include <unistd.h>

void	ft_rev_int_tab(int *tab, int size)
{
	int i;

	i = 0;
	while (i < size / 2)
	{
		i++;
		tab[i - 1] ^= tab[size - i];
		tab[size - i] ^= tab[i - 1];
		tab[i - 1] ^= tab[size - i];
	}
}
